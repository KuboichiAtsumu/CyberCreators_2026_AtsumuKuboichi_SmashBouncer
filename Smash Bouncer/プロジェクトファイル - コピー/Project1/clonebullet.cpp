CCloneBullet::CCloneBullet(int nPriority) : CPlayerBullet(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CCloneBullet::~CCloneBullet()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CCloneBullet::Init()
{
	//バレット初期化処理
	if (FAILED(CPlayerBullet::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CCloneBullet::Uninit()
{
	//バレット終了処理
	CPlayerBullet::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CCloneBullet::Update()
{
	//バレットの更新処理
	CPlayerBullet::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CCloneBullet::Draw()
{
	//バレット描画処理
	CPlayerBullet::Draw();
}
