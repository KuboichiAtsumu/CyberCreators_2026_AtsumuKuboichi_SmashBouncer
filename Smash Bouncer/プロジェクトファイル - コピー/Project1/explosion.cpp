#include "explosion.h"//爆発

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CExplosion::CExplosion(int nPriority) : CBillboard(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CExplosion::~CExplosion()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CExplosion::Init()
{
	//テクスチャ生成
	CTexture* pTex = CManager::GetTexture();//取得
	int nTextureIdx = pTex->Regist("data/TEXTURE/EFFECT/explosion000.png");//登録
	BindTexture(pTex->GetAddress(nTextureIdx));//設定
	SetTextureIdx(nTextureIdx);//ID設定

	//パラメータ設定
	SetDelayAnim(5);//アニメーション遅延
	SetDivisionTex(3, 3);//テクスチャ分割数

	//ビルボード初期化処理
	if (FAILED(CBillboard::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CExplosion::Uninit()
{
	//ビルボード終了処理
	CBillboard::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CExplosion::Update()
{
	//アニメーション処理
	CObject3D::UpdateAnim();

	//テクスチャのアニメーションが１周した場合
	if (GetPatternAnimWidth() >= GetDivisionWidth()
		&& GetPatternAnimHeight() >= GetDivisionHeight())
	{
		//終了処理
		Uninit();
	}
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CExplosion::Draw()
{
	//ビルボード描画処理
	CBillboard::Draw();
}
