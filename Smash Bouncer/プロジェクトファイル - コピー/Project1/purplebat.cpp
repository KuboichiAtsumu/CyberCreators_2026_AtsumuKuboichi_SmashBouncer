#include "purplebat.h"//レッドバットヘッダー

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CPurpleBat::CPurpleBat(int nPriority) : CBat(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CPurpleBat::~CPurpleBat()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CPurpleBat::Init()
{
	//エネミー初期化処理
	if (FAILED(CEnemy::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CPurpleBat::Uninit()
{
	//エネミー終了処理
	CEnemy::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CPurpleBat::Release()
{
	//エネミー解放処理
	CEnemy::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CPurpleBat::Update()
{
	//エディタモードの場合
	if (CManager::GetScene()->GetEditMode())
	{
		//更新せずに処理を抜ける
		return;
	}

	//現在のステージとエネミーのいるステージが一致しない場合
	if (CGame::GetNumStage() != GetPostStageID())
	{
		//更新せずに処理を抜ける
		return;
	}

	//移動処理
	CEnemy::Move();

	//ゴーストの更新処理
	CBat::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CPurpleBat::Draw()
{
	//エネミーの描画処理
	CEnemy::Draw();
}

//===========================================================================================================
// 当たり判定
//===========================================================================================================
bool CPurpleBat::Collision(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	//エネミーの当たり判定
	return CEnemy::Collision(pos, size);
}

//===========================================================================================================
// ヒット処理
//===========================================================================================================
void CPurpleBat::Hit()
{
	//エネミーのヒット処理
	CEnemy::Hit();
}
