#include "pillar.h"//柱

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CPillar::CPillar(int nPriority) : CEffect3D(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CPillar::~CPillar()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CPillar::Init()
{
	//パラメータ設定
	SetDivisionTex(4, 3);//テクスチャ分割数
	SetDelayAnim(1);//アニメーション遅延

	//3Dエフェクト初期化処理
	if (FAILED(CEffect3D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CPillar::Uninit()
{
	//3Dエフェクト終了処理
	CEffect3D::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CPillar::Release()
{
	//3Dエフェクト解放処理
	CEffect3D::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CPillar::Update()
{
	//3Dエフェクト更新処理
	CEffect3D::Update();

	//アニメーション処理
	CObject3D::UpdateAnim();

	//テクスチャのアニメーションが１周した場合
	if (GetPatternAnimWidth() >= GetDivisionWidth()
		&& GetPatternAnimHeight() >= GetDivisionHeight())
	{
		//終了処理
		Uninit();
	}
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CPillar::Draw()
{
	//3Dエフェクト描画処理
	CEffect3D::Draw();
}
