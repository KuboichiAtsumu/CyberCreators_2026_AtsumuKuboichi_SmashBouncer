#include "switchbase.h"//スイッチベースのヘッダーファイル

//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CSwitchBase::CSwitchBase(int nPriority) : CGimmick(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CSwitchBase::~CSwitchBase()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CSwitchBase::Init()
{
	//ギミック初期化処理
	if (FAILED(CGimmick::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CSwitchBase::Uninit()
{
	//ギミック終了処理
	CGimmick::Uninit();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CSwitchBase::Update()
{
	//ギミック更新処理
	CGimmick::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CSwitchBase::Draw()
{
	//エディタモードの場合
	if (CManager::GetScene()->GetEditMode())
	{//描画せずに処理を抜ける
		return;
	}

	//ギミック描画処理
	CGimmick::Draw();
}
