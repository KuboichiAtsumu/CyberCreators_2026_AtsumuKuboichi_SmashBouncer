//===========================================================================================================
// コンストラクタ
//===========================================================================================================
CBlueSlime::CBlueSlime(int nPriority) : CSlime(nPriority)
{
}

//===========================================================================================================
// デストラクタ
//===========================================================================================================
CBlueSlime::~CBlueSlime()
{
}

//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CBlueSlime::Init()
{
	//Xファイルオブジェクト初期化処理
	if (FAILED(CEnemy::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CBlueSlime::Uninit()
{
	//エネミー終了処理
	CSlime::Uninit();
}

//===========================================================================================================
// 解放処理
//===========================================================================================================
void CBlueSlime::Release()
{
	//エネミー解放処理
	CSlime::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CBlueSlime::Update()
{
	//エディタモードの場合
	if (CManager::GetScene()->GetEditMode())
	{
		//更新せずに処理を抜ける
		return;
	}

	//現在のステージとエネミーのいるステージが一致しない場合
	if (CGame::GetNumStage() != GetPostStageID())
	{
		//更新せずに処理を抜ける
		return;
	}

	//エネミーの移動処理
	CEnemy::Move();

	//スライムの更新処理
	CSlime::Update();
}

//===========================================================================================================
// 描画処理
//===========================================================================================================
void CBlueSlime::Draw()
{
	//エネミーの描画処理
	CSlime::Draw();
}

//===========================================================================================================
// 当たり判定
//===========================================================================================================
bool CBlueSlime::Collision(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	//エネミーの当たり判定
	return CEnemy::Collision(pos, size);
}

//===========================================================================================================
// ヒット処理
//===========================================================================================================
void CBlueSlime::Hit()
{
	//エネミーのヒット処理
	CEnemy::Hit();
}
