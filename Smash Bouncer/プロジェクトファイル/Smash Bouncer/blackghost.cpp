{
	//エネミー解放処理
	CEnemy::Release();
}

//===========================================================================================================
// 更新処理
//===========================================================================================================
void CBlackGhost::Update()
{
	//エディタモードの場合
	if (CManager::GetScene()->GetEditMode())
	{
		//更新せずに処理を抜ける
		return;
	}

	//現在のステージとエネミーのいるステージが一致しない場合
	if (CGame::GetNumStage() != GetPostStageID())
	{
		//更新せずに処理を抜ける
		return;
	}

	//移動処理
	CEnemy::Move();

	//ゴーストの更新処理
	CGhost::Update();

	//現在の向き情報を取得
	D3DXVECTOR3 rot = GetRot();

	//正面を向いていない場合
	if (rot.y != D3DX_PI * 0.5f)
	{
		//正面を向くように修正
		rot.y = D3DX_PI * 0.5f;
		SetRot(rot);
	}
}

