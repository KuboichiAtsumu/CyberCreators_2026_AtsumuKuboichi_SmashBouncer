//===========================================================================================================
// 初期化処理
//===========================================================================================================
HRESULT CBlackGhost::Init()
{
	//エネミー初期化処理
	if (FAILED(CEnemy::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===========================================================================================================
// 終了処理
//===========================================================================================================
void CBlackGhost::Uninit()
{
	//エネミー終了処理
	CEnemy::Uninit();
}

